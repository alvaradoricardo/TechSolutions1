    private void jtxtCodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtCodigoKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            if (p.buscarProducto(jtxtCodigo.getText()) == 1) {

                
                jtxtNombre.setText(p.getNombre());
                jtxtDescripcion.setText(p.getDescripcion());
                jtxtPrecioV.setText(Double.toString(p.getPrecioV()));

              
                        
                
            } else {
                JOptionPane.showMessageDialog(null, "Producto No Encontrado", "Alerta", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jtxtCodigoKeyPressed

